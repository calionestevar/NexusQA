FString FLCARSHTMLGenerator::GenerateTestSummarySection(const FReportData& Data)
{
	float PassRate = Data.TotalTests > 0 ? (float)Data.PassedTests / Data.TotalTests * 100.0f : 0.0f;
	// Clamp PassRate to valid range [0, 100]
	PassRate = FMath::Clamp(PassRate, 0.0f, 100.0f);
	
	FString HTML = TEXT(R"(
        <!-- Summary Section -->
        <section class="summary-section">
            <h2>[ Executive Summary ]</h2>
            <div class="stats-grid">
                <div class="stat-card">
                    <div class="stat-label">TOTAL TESTS</div>
                    <div class="stat-value">)");
	HTML += FString::FromInt(Data.TotalTests);
	HTML += TEXT(R"(</div>
                </div>
                <div class="stat-card passed">
                    <div class="stat-label">PASSED</div>
                    <div class="stat-value">)");
	HTML += FString::FromInt(Data.PassedTests);
	HTML += TEXT(R"(</div>
                </div>
                <div class="stat-card failed">
                    <div class="stat-label">FAILED</div>
                    <div class="stat-value">)");
	HTML += FString::FromInt(Data.FailedTests);
	HTML += TEXT(R"(</div>
                </div>
                <div class="stat-card">
                    <div class="stat-label">PASS RATE</div>
                    <div class="stat-value">)");
	HTML += FString::Printf(TEXT("%.1f%%"), PassRate);
	HTML += TEXT(R"(</div>
                </div>
            </div>
            <div class="stats-grid" style="margin-top: 20px;">
                <div class="stat-card">
                    <div class="stat-label">TOTAL RUNTIME</div>
                    <div class="stat-value">)");
	HTML += FString::Printf(TEXT("%.2fs"), Data.TotalDuration);
	HTML += TEXT(R"(</div>
                </div>
            </div>
        </section>
)");
	
	return HTML;
}

FString FLCARSHTMLGenerator::GenerateAPIMetricsSection(const FReportData& Data)
{
	if (Data.APIMetrics.TotalRequests == 0)
	{
		return TEXT("");
	}
	
	float APISuccessRate = Data.APIMetrics.TotalRequests > 0 ? 
		(float)Data.APIMetrics.SuccessfulRequests / Data.APIMetrics.TotalRequests * 100.0f : 0.0f;
	
	FString HTML = TEXT(R"(
        <!-- API Metrics Section -->
        <section class="api-section">
            <h2>[ API Testing Metrics ]</h2>
            
            <div class="stats-grid">
                <div class="stat-card">
                    <div class="stat-label">TOTAL REQUESTS</div>
                    <div class="stat-value">)");
	HTML += FString::FromInt(Data.APIMetrics.TotalRequests);
	HTML += TEXT(R"(</div>
                </div>
                <div class="stat-card passed">
                    <div class="stat-label">SUCCESSFUL</div>
                    <div class="stat-value">)");
	HTML += FString::FromInt(Data.APIMetrics.SuccessfulRequests);
	HTML += TEXT(R"(</div>
                </div>
                <div class="stat-card failed">
                    <div class="stat-label">FAILED</div>
                    <div class="stat-value">)");
	HTML += FString::FromInt(Data.APIMetrics.FailedRequests);
	HTML += TEXT(R"(</div>
                </div>
                <div class="stat-card">
                    <div class="stat-label">AVG RESPONSE TIME</div>
                    <div class="stat-value">)");
	HTML += FString::Printf(TEXT("%.1fms"), Data.APIMetrics.AvgResponseTimeMs);
	HTML += TEXT(R"(</div>
                    <div class="pass-rate">Success Rate: )");
	HTML += FString::Printf(TEXT("%.1f%%"), APISuccessRate);
	HTML += TEXT(R"(</div>
                </div>
            </div>
            
            <!-- Status Code Distribution -->
            <div class="chart-container">
                <h3 class="chart-title">HTTP Status Code Distribution</h3>
                <div class="status-code-grid">
)");
	
	for (const auto& Pair : Data.APIMetrics.StatusCodeDistribution)
	{
		int32 StatusCode = Pair.Key;
		int32 Count = Pair.Value;
		FString StatusClass = (StatusCode >= 200 && StatusCode < 300) ? TEXT("success") : TEXT("error");
		
		HTML += TEXT(R"(                    <div class="status-code-item )");
		HTML += StatusClass;
		HTML += TEXT(R"(">
                        <div class="status-code">");
		HTML += FString::FromInt(StatusCode);
		HTML += TEXT(R"(</div>
                        <div class="status-count">&times; )");
		HTML += FString::FromInt(Count);
		HTML += TEXT(R"(</div>
                    </div>
)");
	}
	
	HTML += TEXT(R"(                </div>
            </div>
            
            <!-- Endpoint Response Times -->
            <div class="chart-container">
                <h3 class="chart-title">Endpoint Response Times</h3>
                <ul class="endpoint-list">
)");
	
	for (const auto& Pair : Data.APIMetrics.EndpointResponseTimes)
	{
		const FString& Endpoint = Pair.Key;
		float ResponseTime = Pair.Value;
		FString StatusClass = ResponseTime < 500.0f ? TEXT("") : TEXT("failed");
		
		HTML += TEXT(R"(                    <li class="endpoint-item )");
		HTML += StatusClass;
		HTML += TEXT(R"(">
                        <span class="endpoint-name">)");
		HTML += Endpoint;
		HTML += TEXT(R"(</span>
                        <span class="endpoint-time">)");
		HTML += FString::Printf(TEXT("%.1f ms"), ResponseTime);
		HTML += TEXT(R"(</span>
                    </li>
)");
	}
	
	HTML += TEXT(R"(                </ul>
            </div>
        </section>
)");
	
	return HTML;
}

FString FLCARSHTMLGenerator::GeneratePerformanceMetricsSection(const FReportData& Data)
{
	if (Data.PerfMetrics.AvgFPS == 0.0f)
	{
		return TEXT("");
	}
	
	FString HTML = TEXT(R"(
        <!-- Performance Metrics Section -->
        <section class="performance-section">
            <h2>[ Performance Metrics ]</h2>
            <div class="stats-grid">
                <div class="stat-card">
                    <div class="stat-label">AVERAGE FPS</div>
                    <div class="stat-value">)");
	HTML += FString::Printf(TEXT("%.1f"), Data.PerfMetrics.AvgFPS);
	HTML += TEXT(R"(</div>
                </div>
                <div class="stat-card">
                    <div class="stat-label">MIN / MAX FPS</div>
                    <div class="stat-value">)");
	HTML += FString::Printf(TEXT("%.1f / %.1f"), Data.PerfMetrics.MinFPS, Data.PerfMetrics.MaxFPS);
	HTML += TEXT(R"(</div>
                </div>
                <div class="stat-card">
                    <div class="stat-label">PEAK MEMORY</div>
                    <div class="stat-value">)");
	HTML += FString::Printf(TEXT("%.1f MB"), Data.PerfMetrics.PeakMemoryMB);
	HTML += TEXT(R"(</div>
                </div>
                <div class="stat-card">
                    <div class="stat-label">HITCHES DETECTED</div>
                    <div class="stat-value">)");
	HTML += FString::FromInt(Data.PerfMetrics.HitchCount);
	HTML += TEXT(R"(</div>
                </div>
            </div>
        </section>
)");
	
	return HTML;
}

FString FLCARSHTMLGenerator::GenerateTestDetailsSection(const FReportData& Data)
{
	FString HTML = TEXT(R"(
        <!-- Test Details Section -->
        <section class="details-section">
            <h2>[ Test Results ]</h2>
)");
	
	for (const FTestResult& Test : Data.Tests)
	{
		FString StatusClass = Test.Status;
		
		HTML += TEXT(R"(            <div class="test-item )");
		HTML += StatusClass.ToLower();
		HTML += TEXT(R"(">
                <div class="test-header">
                    <span class="test-name">)");
		HTML += Test.Name;
		HTML += TEXT(R"(</span>
                    <span class="test-status )");
		HTML += Test.Status;
		HTML += TEXT(R"(">)");
		HTML += Test.Status;
		HTML += TEXT(R"(</span>
                </div>
                <div class="test-meta">
                    Duration: )");
		HTML += FString::Printf(TEXT("%.3fs"), Test.DurationSeconds);
		
		if (!Test.TraceID.IsEmpty())
		{
			HTML += TEXT(" | Trace ID: ");
			HTML += Test.TraceID;
		}
		
		HTML += TEXT(R"(</div>
)");
		
		if (!Test.ErrorMessage.IsEmpty())
		{
			HTML += TEXT(R"(                <div class="test-error">
                    [ERROR] )");
			HTML += Test.ErrorMessage;
			HTML += TEXT(R"(
                </div>
)");
		}
		
		if (Test.Artifacts.Num() > 0)
		{
			HTML += TEXT(R"(                <div class="test-artifacts">
                    <strong>Artifacts:</strong>
)");
			for (const FString& Artifact : Test.Artifacts)
			{
				HTML += TEXT(R"(                    <a href=")");
				HTML += Artifact;
				HTML += TEXT(R"(" class="artifact-link">ðŸ“Ž )");
				HTML += FPaths::GetCleanFilename(Artifact);
				HTML += TEXT(R"(</a>
)");
			}
			HTML += TEXT(R"(                </div>
)");
		}
		
		HTML += TEXT(R"(            </div>
)");
	}
	
	HTML += TEXT(R"(        </section>
)");
	
	return HTML;
}
